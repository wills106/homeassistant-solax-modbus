    async def async_determineInverterType(self, hub, configdict):
        _LOGGER.info(f"{hub.name}: trying to determine inverter type")
        seriesnumber = await async_read_serialnr(hub, 33004, swapbytes=False)
        if not seriesnumber:
            _LOGGER.error(f"{hub.name}: cannot find serial number, even not for other Inverter")
            seriesnumber = "unknown"

        # derive invertertype from seriiesnumber
        if seriesnumber.startswith("1801"):
            invertertype = HYBRID | X1  # PV Only S6-GR1P 1-3K
        elif seriesnumber.startswith("1802"):
            invertertype = HYBRID | X1  # PV Only S6-GR1P 2.5-6K
        elif seriesnumber.startswith("0602"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3kW - 48v
        elif seriesnumber.startswith("0102"):
            invertertype = HYBRID | X1  # AC? Gen5 3kW - 48v
        elif seriesnumber.startswith("010F"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3kW - 48v
        elif seriesnumber.startswith("110F"):
            invertertype = HYBRID | X1  # Hybrid Gen5 5kW - 48v
        elif seriesnumber.startswith("114F"):
            invertertype = HYBRID | X1  # Hybrid Gen5 6K - 48V
        elif seriesnumber.startswith("134F"):
            invertertype = HYBRID | X1  # Hybrid Gen5 5kW - 48V
        elif seriesnumber.startswith("140C"):
            invertertype = HYBRID | X1  # Hybrid Gen5 5kW - HV
        elif seriesnumber.startswith("143"):
            invertertype = HYBRID | X1  # Hybrid Gen5 5kW - 48V
        elif seriesnumber.startswith("160F3"):
            invertertype = HYBRID | X1  # Hybrid Gen5 5kW - 48v
        elif seriesnumber.startswith("160F4"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3.6kW - 48v
        elif seriesnumber.startswith("160F5"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3.6kW - 48v
        elif seriesnumber.startswith("103305"):
            invertertype = HYBRID | X3 | MPPT4  # Hybrid Gen6  8kW - HV
        elif seriesnumber.startswith("103306"):
            invertertype = HYBRID | X3 | MPPT4  # Hybrid Gen6  10kW - HV
        elif seriesnumber.startswith("110C"):
            invertertype = HYBRID | X3  # Hybrid Gen5 0CA2 / 0C92 10kW - HV
        elif seriesnumber.startswith("114C"):
            invertertype = HYBRID | X3  # Hybrid Gen5 10kW - HV
        elif seriesnumber.startswith("1805"):
            invertertype = HYBRID | X3  # PV Only Gen5 5-20kW
        elif seriesnumber.startswith("6031"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3105 / 3122 Model 6kW - 48V
        elif seriesnumber.startswith("1031"):
            invertertype = HYBRID | X1  # Hybrid Gen5 3104 Model 5kW - 48V
        # elif seriesnumber.startswith('abc123'):  invertertype = PV | X3 # Comment

        else:
            invertertype = 0
            _LOGGER.error(f"unrecognized {hub.name} inverter type - serial number : {seriesnumber}")

        if invertertype > 0:
            read_eps = configdict.get(CONF_READ_EPS, DEFAULT_READ_EPS)
            read_dcb = configdict.get(CONF_READ_DCB, DEFAULT_READ_DCB)
            if read_eps:
                invertertype = invertertype | EPS
            if read_dcb:
                invertertype = invertertype | DCB

        return invertertype

    def matchInverterWithMask(self, inverterspec, entitymask, serialnumber="not relevant", blacklist=None):
        # returns true if the entity needs to be created for an inverter
        genmatch = ((inverterspec & entitymask & ALL_GEN_GROUP) != 0) or (entitymask & ALL_GEN_GROUP == 0)
        xmatch = ((inverterspec & entitymask & ALL_X_GROUP) != 0) or (entitymask & ALL_X_GROUP == 0)
        hybmatch = ((inverterspec & entitymask & ALL_TYPE_GROUP) != 0) or (entitymask & ALL_TYPE_GROUP == 0)
        epsmatch = ((inverterspec & entitymask & ALL_EPS_GROUP) != 0) or (entitymask & ALL_EPS_GROUP == 0)
        dcbmatch = ((inverterspec & entitymask & ALL_DCB_GROUP) != 0) or (entitymask & ALL_DCB_GROUP == 0)
        mpptmatch = ((inverterspec & entitymask & ALL_MPPT_GROUP) != 0) or (entitymask & ALL_MPPT_GROUP == 0)
        blacklisted = False
        if blacklist:
            for start in blacklist:
                if serialnumber.startswith(start):
                    blacklisted = True
        return (genmatch and xmatch and hybmatch and epsmatch and dcbmatch and mpptmatch) and not blacklisted


