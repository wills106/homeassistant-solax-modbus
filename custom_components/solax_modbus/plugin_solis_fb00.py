def value_function_timing_on_off(bit: int, state: int, descr: str, datadict: dict):
    value = datadict.get(descr, 0)
    _LOGGER.debug(f">>> Old value of {descr}: {value}")
    new_value = (value & ~(1 << bit)) | (state << bit)
    return new_value


